#include <stdio.h>

void print_hash_of_f() {
  printf("######\n");
  printf("#\n");
  printf("#\n");
  printf("#####\n");
  printf("#\n");
  printf("#\n");
  printf("#\n\n");
}

void print_hash_of_c() {
  printf("    ######\n");
  printf("  ##      ##\n");
  printf(" #\n");
  printf(" #\n");
  printf(" #\n");
  printf(" #\n");
  printf(" #\n");
  printf("  ##      ##\n");
  printf("    ######\n\n");
}

void print_hash_of_k() {
  printf("#   #\n");
  printf("#  #\n");
  printf("# #\n");
  printf("##\n");
  printf("# #\n");
  printf("#  #\n");
  printf("#   #\n\n");
}

void print_hash_of_u() {
  printf("#        #\n");
  printf("#        #\n");
  printf("#        #\n");
  printf("#        #\n");
  printf("#        #\n");
  printf("#        #\n");
  printf("#        #\n");
  printf("#        #\n");
  printf(" ########\n\n");
}



void main() {
  printf("Print Characters using Hash (#) symbols\n");

  print_hash_of_f();
  print_hash_of_c();
  print_hash_of_k();
  print_hash_of_u();
}
